package model.elements;

import java.awt.Paint;
import java.awt.Stroke;

import painters.ElementPainter;

public class DocumentElement {
	
	protected Paint paint;
	
	protected Stroke stroke;
	
	protected String name;

	protected ElementPainter elementPainter;
	
	
	
	
	public DocumentElement(Paint paint, Stroke stroke) {
		super();
		this.paint = paint;
		this.stroke = stroke;
	}

	public Paint getPaint() {
		return paint;
	}

	public void setPaint(Paint paint) {
		this.paint = paint;
	}

	public Stroke getStroke() {
		return stroke;
	}

	public void setStroke(Stroke stroke) {
		this.stroke = stroke;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public ElementPainter getPainter() {
		return elementPainter;
	}

	public void setPainter(ElementPainter elementPainter) {
		this.elementPainter = elementPainter;
	}
	
	
	
	

}
